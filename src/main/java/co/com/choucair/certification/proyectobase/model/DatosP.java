package co.com.choucair.certification.proyectobase.model;

public class DatosP {

    private String strNombre;
    private String strApellido;
    private String strEmail;
    private String strClave;
    private String strConfirmarClave;

    public String getStrNombre() {
        return strNombre;
    }

    public void setStrNombre(String strNombre) {
        this.strNombre = strNombre;
    }

    public String getStrApellido() {
        return strApellido;
    }

    public void setStrApellido(String strApellido) {
        this.strApellido = strApellido;
    }

    public String getStrEmail() {
        return strEmail;
    }

    public void setStrEmail(String strEmail) {
        this.strEmail = strEmail;
    }

    public String getStrClave() {
        return strClave;
    }

    public void setStrClave(String strClave) {
        this.strClave = strClave;
    }

    public String getStrConfirmarClave() {
        return strConfirmarClave;
    }

    public void setStrConfirmarClave(String strConfirmarClave) {
        this.strConfirmarClave = strConfirmarClave;
    }

}
